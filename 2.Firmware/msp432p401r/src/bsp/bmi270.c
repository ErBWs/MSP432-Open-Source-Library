// 参数说明     gyro_value              // 任意轴的陀螺仪数据
// 返回参数     void
// 使用示例     float data = bmi270_gyro_transition(bmi270_gyro_x);  // 单位为°/s
// 备注信息
//-------------------------------------------------------------------------------------------------------------------
float bmi270_gyro_transition (int16_t gyro_value)
{
    float gyro_data = 0;
    switch(BMI270_GYR_SAMPLE)
    {
        case 0x00: gyro_data = (float)gyro_value / 16.4f;  break;  //  0x00 陀螺仪量程为:±2000dps      获取到的陀螺仪数据除以16.4          可以转化为带物理单位的数据，单位为：°/s
        case 0x01: gyro_data = (float)gyro_value / 32.8f;  break;  //  0x01 陀螺仪量程为:±1000dps      获取到的陀螺仪数据除以32.8          可以转化为带物理单位的数据，单位为：°/s
        case 0x02: gyro_data = (float)gyro_value / 65.6f;  break;  //  0x02 陀螺仪量程为:±500 dps      获取到的陀螺仪数据除以65.6          可以转化为带物理单位的数据，单位为：°/s
        case 0x03: gyro_data = (float)gyro_value / 131.2f; break;  //  0x03 陀螺仪量程为:±250 dps      获取到的陀螺仪数据除以131.2         可以转化为带物理单位的数据，单位为：°/s
        case 0x04: gyro_data = (float)gyro_value / 262.4f; break;  //  0x04 陀螺仪量程为:±125 dps      获取到的陀螺仪数据除以262.4         可以转化为带物理单位的数据，单位为：°/s
        default: break;
    }
    return gyro_data;
}

//-------------------------------------------------------------------------------------------------------------------
// 函数简介     初始化 BMI270
// 参数说明     void
// 返回参数     uint8_t           1-初始化失败 0-初始化成功
// 使用示例     bmi270_init();
// 备注信息
//-------------------------------------------------------------------------------------------------------------------
uint8_t bmi270_init (void)
{
    uint8_t return_state = 0;
    Delay_ms(20);                                                    // 等待设备上电成功
#if BMI270_USE_SOFT_IIC
    SoftIIC_Init(&bmi270_iic_struct, BMI270_DEV_ADDR, BMI270_SOFT_IIC_DELAY, BMI270_SCL_PORT, BMI270_SCL_PIN, BMI270_SDA_PORT, BMI270_SDA_PIN);        // 配置 BMI270 的 IIC端口
#else
    SPI_Init(SPI_MODE0, SPI_CS_MODE_NULL, BMI270_SPI_SPEED, BMI270_SPC_PIN, BMI270_SDI_PIN, BMI270_SDO_PIN, SPI_CS_NULL);
    GPIO_setAsOutputPin(BMI270_CS_PORT, BMI270_CS_PIN);
    bmi270_read_register(BMI270_CHIP_ID);                                                                                   // 读取一下设备ID，将设备设置为SPI模式
#endif
    do{
        if(bmi270_self_check())                                           // BMI270自检
        {
            // 如果程序在输出了断言信息 并且提示出错位置在这里
            // 那么就是 bmi270 自检出错并超时退出了
            // 检查一下接线有没有问题 如果没问题可能就是坏了
            return_state = 1;
            break;
        }
        bmi270_write_register(BMI270_PWR_CONF, 0x00);                   // 关闭高级省电模式
        Delay_ms(1);
        bmi270_write_register(BMI270_INIT_CTRL, 0x00);                  // 开始对模块进行初始化配置
        bmi270_write_registers(BMI270_INIT_DATA, bmi270ConfigArray, sizeof(bmi270ConfigArray));                       // 输出配置文件
        bmi270_write_register(BMI270_INIT_CTRL, 0x01);                  // 初始化配置结束
        Delay_ms(20);
        if(bmi270_read_register(BMI270_INT_STA) == 0)                   // 检查是否配置完成
        {
            // 如果程序在输出了断言信息 并且提示出错位置在这里
            // 那么就是 bmi270 配置初始化文件出错了
            // 检查一下接线有没有问题 如果没问题可能就是坏了
            return_state = 1;
            break;
        }
        bmi270_write_register(BMI270_PWR_CTRL, 0x0E);                   // 开启性能模式  使能陀螺仪、加速度、温度传感器
        bmi270_write_register(BMI270_ACC_CONF, 0xA7);                   // 加速度采集配置 性能模式 正常采集 50Hz 采样频率
        bmi270_write_register(BMI270_GYR_CONF, 0xA9);                   // 陀螺仪采集配置 性能模式 正常采集 200Hz采样频率
        bmi270_write_register(BMI270_ACC_RANGE, BMI270_ACC_SAMPLE);   // 加速度量程配置 配置量程为:±8g
        bmi270_write_register(BMI270_GYR_RANGE, BMI270_GYR_SAMPLE);   // 陀螺仪量程配置 配置量程为:±2000dps

        // BMI270_GYR_RANGE寄存器
        // 设置为:0x00 加速度计量程为:±2g          获取到的加速度计数据 除以16384      可以转化为带物理单位的数据，单位：g(m/s^2)
        // 设置为:0x01 加速度计量程为:±4g          获取到的加速度计数据 除以8192       可以转化为带物理单位的数据，单位：g(m/s^2)
        // 设置为:0x02 加速度计量程为:±8g          获取到的加速度计数据 除以4096       可以转化为带物理单位的数据，单位：g(m/s^2)
        // 设置为:0x03 加速度计量程为:±16g         获取到的加速度计数据 除以2048       可以转化为带物理单位的数据，单位：g(m/s^2)

        // BMI270_ACC_SAMPLE寄存器
        // 设置为:0x00 陀螺仪量程为:±2000dps      获取到的陀螺仪数据除以16.4          可以转化为带物理单位的数据，单位为：°/s
        // 设置为:0x01 陀螺仪量程为:±1000dps      获取到的陀螺仪数据除以32.8          可以转化为带物理单位的数据，单位为：°/s
        // 设置为:0x02 陀螺仪量程为:±500 dps      获取到的陀螺仪数据除以65.6          可以转化为带物理单位的数据，单位为：°/s
        // 设置为:0x03 陀螺仪量程为:±250 dps      获取到的陀螺仪数据除以131.2         可以转化为带物理单位的数据，单位为：°/s
        // 设置为:0x04 陀螺仪量程为:±125 dps      获取到的陀螺仪数据除以262.4         可以转化为带物理单位的数据，单位为：°/s
    }while(0);
    return return_state;
}



